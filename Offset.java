package poppyfanboy.snakegame;

/**
 * Class "Offset"
 * A simple class that allows to store offsets of
 * the coordinates by x and y axises simultaneously
 */

public class Offset {
    public int offsetX;
    public int offsetY;

    public Offset(int x, int y) {
        this.offsetX = x;
        this.offsetY = y;
    }
}
