package poppyfanboy.snakegame.logic;

/**
 * Class "Offset"
 * A simple class that allows to store offsets of
 * the coordinates by x and y axises simultaneously
 *
 * @author PoppyFanboy
 */

class Offset {
    int offsetX;
    int offsetY;

    Offset(int x, int y) {
        this.offsetX = x;
        this.offsetY = y;
    }
}